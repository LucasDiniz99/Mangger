    private void carregarDados(Integer id) {
        DadosMidia midia = gerViews.getGerDados().buscarId(id);
        if (midia == null) {
            return;
        }

        this.Fld_ID.setText(midia.id + "");
        this.Fld_Nome.setText(midia.nome + "");
        this.Fld_NomeAlt.setText(midia.nomeAlt + "");
        this.Fld_NotaMAL.setText(midia.notaMAL + "");
        this.Fld_DataLanc.setText(midia.dataLancamento + "");
        this.Fld_TipoObra.setText(midia.tipo + "");
        this.Fld_CapLancados.setText(midia.capitulos + "");
        this.Fld_StatusPub.setText(midia.status + "");
        this.Fld_Generos.setText(midia.generos + "");
        this.Fld_Altor.setText(midia.altor + "");
        this.TxtA_Sinopse.setText(midia.id + "");
        this.Fld_FonteSinopse.setText(midia.id + "");
        this.Fld_ProxCapLer.setText(midia.id + "");
        this.Fld_DataLeitura.setText(midia.id + "");
    }

    private DadosMidia getDados() {
        DadosMidia midia = new DadosMidia();
        midia.id = Integer.valueOf(this.Fld_ID.getText());
        midia.nome = this.Fld_Nome.getText();
        midia.nomeAlt = this.Fld_NomeAlt.getText();
        midia.notaMAL = Double.valueOf(this.Fld_NotaMAL.getText());
        midia.dataLancamento = this.Fld_DataLanc.getText();
        midia.tipo = this.Fld_TipoObra.getText();
        midia.capitulos = Integer.valueOf(this.Fld_CapLancados.getText());
        midia.status = this.Fld_StatusPub.getText();
        midia.generos = this.Fld_Generos.getText();
        midia.altor = this.Fld_Altor.getText();
        midia.sinopse = this.TxtA_Sinopse.getText();
        midia.fonteSinopse = this.Fld_FonteSinopse.getText();
        midia.proxCapLeitura = Double.valueOf(this.Fld_ProxCapLer.getText());
        midia.leituraCompleta = midia.proxCapLeitura >= midia.capitulos ? "Completado" : "Incompleto";
        midia.dataDeLeitura = this.Fld_DataLeitura.getText();
        return midia;
    }

