    private void Btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SalvarActionPerformed
        DadosMidia midia = getDados();
        Integer id = Integer.valueOf(this.Fld_ID.getText());

        if (alterar) {
            gerViews.getGerDados().alterarMidia(id, midia);
            if (!gerNotifi.notificarAlteracao())
                return;
        } else {
            gerViews.getGerDados().inserirMidia(midia);
        }
        
        gerViews.alterarDados();
        gerViews.voltarView();
    }//GEN-LAST:event_Btn_SalvarActionPerformed


